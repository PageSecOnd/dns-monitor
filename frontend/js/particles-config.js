// Responsive particle configuration
function updateParticleConfig() {
    const width = window.innerWidth;
    let particleCount = 100;
    
    if (width < 768) {
        particleCount = 50;
    } else if (width < 1024) {
        particleCount = 75;
    }
    
    // Update particle count
    if (window.pJSDom && window.pJSDom[0]) {
        window.pJSDom[0].pJS.particles.number.value = particleCount;
        window.pJSDom[0].pJS.fn.particlesRefresh();
    }
}

// Update particles on window resize
window.addEventListener('resize', updateParticleConfig);

// Initialize particle configuration
document.addEventListener('DOMContentLoaded', updateParticleConfig);